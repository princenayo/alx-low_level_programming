#include "lists.h"
#include <stdio.h>

/**
 * listint_len - Returns the number of elements in the lists
 *
 * @h: A pointer to the head of the list.
 *
 * Return: The number of elements in the list.
 */
size_t listint_len(const listint_t *h)
{
	size_t number = 0;

	while (h)
	{
		number++;
		h = h->next;
	}

	return (number);
}
